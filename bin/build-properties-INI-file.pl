#!/usr/bin/env perl

use strict;
use warnings;

use Getopt::Long;
use Pod::Usage;
use IO::All -utf8;

use HON::I18N::Converter;

=head1 NAME

build-properties-INI-file.pl

=head1 DESCRIPTION

build the INI file and the .ini file for the Search Engine language

=head1 SYNOPSIS

./build-properties-INI-file.pl --help

./build-properties-INI-file.pl --i18n=i18n/TestConversionENES.xls --output=/Users/chahlalsamia/Desktop/ENES.ini


=head1 ARGUMENTS

=over 2

=item --i18n=i18n/TestConversionENES.xls

The excel file with all the language label -> traduction

=item --output=/Users/chahlalsamia/Desktop/ENES.ini

The output .ini folder

=cut

my ( $help, $i18n, $output );

GetOptions(
  "i18n=s"      => \$i18n,
  "output=s"    => \$output, 
  "help"        => \$help,
  )
  || pod2usage(2);

if ( $help || !$i18n ) {
  pod2usage(1);
  exit(0);
}

my $parser = HON::I18N::Converter->new(excel => $i18n );

my $header = <<EOS;
; Automatically generated by the HON-I18N-Converter project
; Please do not modify this file directly!!

EOS

$parser->build_properties_file('INI', $output, $header);
